# Создание пустого списка для хранения значений
array = []

print("Введите 10 значений для массива:")

# Заполнение массива значениями, вводимыми с клавиатуры
for i in range(10):
    value = int(input(f"Значение {i + 1}: "))
    array.append(value)

# Вывод заполненного массива
print("Заполненный массив:", array)
