# Запрашиваем значение a
a = int(input("Введите a (a <= 200): "))

# Убедимся, что a <= 200
if a > 200:
    print("Значение a должно быть не больше 200.")
else:
    # Вычисляем сумму всех целых чисел от a до 200
    total_sum = sum(range(a, 201))

    # Вычисляем среднее арифметическое
    average = total_sum / (201 - a)

    # Выводим результат
    print("Среднее арифметическое всех целых чисел от", a, "до 200:", average)
