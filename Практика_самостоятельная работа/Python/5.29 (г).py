# Запрашиваем значения a и b
a = int(input("Введите a: "))
b = int(input("Введите b: "))

# Проверяем, что a не меньше b
if a > b:
    print("a должно быть меньше или равно b. Перезапустите программу с корректными значениями.")
else:
    # Вычисляем сумму всех целых чисел от a до b
    total_sum = sum(range(a, b + 1))

    # Вычисляем среднее арифметическое
    average = total_sum / (b - a + 1)

    # Выводим результат
    print("Среднее арифметическое всех целых чисел от", a, "до", b, ":", average)
