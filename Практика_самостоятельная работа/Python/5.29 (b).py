# Запрашиваем значение b
b = int(input("Введите b (b >= 150): "))

# Убедимся, что b >= 150
if b < 150:
    print("Значение b должно быть не меньше 150.")
else:
    # Вычисляем сумму всех целых чисел от 150 до b
    total_sum = sum(range(150, b + 1))

    # Вычисляем среднее арифметическое
    average = total_sum / (b - 149)

    # Выводим результат
    print("Среднее арифметическое всех целых чисел от 150 до", b, ":", average)
